public class Ejemplo02_04 {
	public static void main(String[] args) {
		int divisionEntera = 7 / 2;
		int modulo = 7 % 2;
		double divisionReal = 7.0 / 2.0;
		System.out.println("División Entera 7/2: " + divisionEntera);
		System.out.println("Módulo 7%2: " + modulo);
		System.out.println("División Real 7.0/2.0: " + divisionReal); 
	}
}