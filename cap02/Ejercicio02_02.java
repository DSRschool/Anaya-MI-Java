/* Ejercicio 2.2 - Escribe un programa que imprima por pantalla cuántos argumentos 
 * ha recibido: "He recibido … argumentos."
 */

public class Ejercicio02_02 {
	public static void main(String[] args) {
		// length es un atributo de los arrays que nos indica cuántos elementos tiene
		System.out.println("He recibido " + args.length + " argumentos.");
	}
}